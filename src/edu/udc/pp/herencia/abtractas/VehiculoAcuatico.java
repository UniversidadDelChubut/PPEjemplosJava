package edu.udc.pp.herencia.abtractas;

public abstract class VehiculoAcuatico extends Vehiculo {
	
	private int eslora;
	private int calado;
	private int manga;
	
	public int getEslora() {
		return eslora;
	}
	
	public int getCalado() {
		return calado;
	}
	
	public int getManga() {
		return manga;
	}
	
}
